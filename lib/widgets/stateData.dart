import 'package:url_launcher/url_launcher.dart';

launchMHdata() async {
  const url = "https://www.covid19india.org/state/MH";
  if (await canLaunch(url)) {
    await launch(url, forceWebView: true, enableJavaScript: true);
  } else {
    throw 'Could not launch $url';
  }
}

